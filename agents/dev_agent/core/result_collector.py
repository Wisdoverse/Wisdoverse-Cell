"""Post-workflow: security scan -> MR -> QA -> status update."""

from __future__ import annotations

from shared.config import settings
from shared.core import GitLabMergeRequestPort
from shared.schemas.event import Event, EventTypes
from shared.utils.logger import get_logger

from ..app.metrics import RETRY_COUNT, TASK_DURATION, TASKS_COMPLETED, TASKS_FAILED
from .notifier import DevNotifier
from .security_scanner import SecurityScanner

logger = get_logger("dev_agent.result_collector")


_WORKSPACE_PATH_KEYS = (
    "workspace_path",
    "workspacePath",
    "worktree_path",
    "worktreePath",
    "working_directory",
    "workingDirectory",
)


class ResultCollector:
    def __init__(
        self,
        repo,
        log_repo,
        gitlab: GitLabMergeRequestPort,
        notifier: DevNotifier,
        security_scanner: SecurityScanner | None = None,
    ):
        self._repo = repo
        self._log_repo = log_repo
        self._gitlab = gitlab
        self._notifier = notifier
        self._scanner = security_scanner or SecurityScanner()

    async def handle_completion(self, task, workflow_status: dict) -> list[Event]:
        """Process workflow completion through the state machine pipeline."""
        events: list[Event] = []
        task_id = task.id
        wp_id = task.wp_id

        await self._repo.update_status(task_id, "security_scanning")
        workspace_path = self._resolve_workspace_path(workflow_status)
        scan_report = await self._scanner.scan(workspace_path)
        if not scan_report.passed:
            await self._repo.update_status(
                task_id,
                "failed",
                error_message=f"Security scan failed: {'; '.join(scan_report.issues)}",
                failed_step="security_scanning",
            )
            TASKS_FAILED.labels(reason="security_scan").inc()
            await self._notifier.notify_task_failed(
                wp_id, f"Security scan blocked MR: {scan_report.issues[0]}"
            )
            return events

        await self._repo.update_status(task_id, "mr_creating")
        source_branch = f"dev/wp-{wp_id}"
        try:
            existing = await self._gitlab.check_existing_mr(source_branch)
            if existing:
                mr_iid = existing["iid"]
                mr_url = existing["web_url"]
                logger.info("mr_already_exists", wp_id=wp_id, mr_iid=mr_iid)
            else:
                mr_data = await self._gitlab.create_mr(
                    source_branch=source_branch,
                    target_branch="main",
                    title=f"[DevAgent] {task.task_title or f'WP#{wp_id}'}",
                    description=(
                        f"Auto-generated by dev_agent\n\n"
                        f"- OP Work Package: #{wp_id}\n"
                        f"- Risk Level: {task.risk_level}\n\n"
                        "## Security Checklist\n"
                        "- [ ] New dependency review\n"
                        "- [ ] Secret/credential leak check\n"
                    ),
                )
                mr_iid = mr_data["iid"]
                mr_url = mr_data["web_url"]

            await self._repo.update_status(
                task_id, "mr_created", mr_iid=mr_iid, mr_url=mr_url
            )
        except Exception as e:
            await self._repo.update_status(
                task_id,
                "failed",
                error_message=str(e),
                failed_step="mr_creating",
            )
            TASKS_FAILED.labels(reason="mr_creation").inc()
            await self._notifier.notify_task_failed(wp_id, f"MR creation failed: {e}")
            return events

        await self._repo.update_status(task_id, "qa_triggered")
        qa_event = Event.create(
            event_type=EventTypes.QA_RUN_REQUESTED,
            source_agent="dev-agent",
            payload={
                "agent_name": self._infer_agent_name(task),
                "level": "all",
                "mr_iid": mr_iid,
                "gitlab_project_id": settings.dev_gitlab_project_id,
                "requested_by": "dev-agent",
            },
        )
        events.append(qa_event)
        await self._repo.update_status(task_id, "reviewing")

        mr_event = Event.create(
            event_type=EventTypes.DEV_MR_CREATED,
            source_agent="dev-agent",
            payload={
                "mr_url": mr_url,
                "wp_id": wp_id,
                "branch": source_branch,
                "risk_level": task.risk_level or "MEDIUM",
            },
        )
        events.append(mr_event)
        await self._notifier.notify_mr_created(
            wp_id, mr_url, task.risk_level or "MEDIUM"
        )

        return events

    async def handle_qa_result(self, task, qa_payload: dict) -> list[Event]:
        """Process QA acceptance result."""
        events: list[Event] = []
        summary = qa_payload.get("summary", {})
        l0_pass = summary.get("l0_gate") == "PASS"

        if l0_pass:
            await self._repo.update_status(task.id, "completed")
            TASKS_COMPLETED.inc()
            # Record task duration metric
            duration_s = 0
            if task.created_at:
                from datetime import UTC, datetime
                duration_s = (datetime.now(UTC) - task.created_at).total_seconds()
                TASK_DURATION.observe(duration_s)
            await self._notifier.notify_task_completed(task.wp_id, task.mr_url or "")
            events.append(
                Event.create(
                    event_type=EventTypes.DEV_TASK_COMPLETED,
                    source_agent="dev-agent",
                    payload={
                        "wp_id": task.wp_id,
                        "mr_url": task.mr_url or "",
                        "duration_s": duration_s,
                    },
                )
            )
        elif task.retry_count < 1:
            RETRY_COUNT.inc()
            await self._repo.update_status(
                task.id,
                "failed",
                error_message="QA L0 failed",
                failed_step="qa",
            )
            # Retry: failed -> planning (allowed by VALID_TRANSITIONS)
            await self._repo.update_status(
                task.id, "planning", retry_count=task.retry_count + 1
            )
            logger.info(
                "qa_retry_triggered", wp_id=task.wp_id, retry=task.retry_count + 1
            )
        else:
            await self._repo.update_status(
                task.id,
                "failed",
                error_message="QA failed after retry",
                failed_step="qa",
            )
            TASKS_FAILED.labels(reason="qa_failed").inc()
            await self._notifier.notify_task_failed(
                task.wp_id, "QA acceptance failed after retry"
            )
            events.append(
                Event.create(
                    event_type=EventTypes.DEV_TASK_FAILED,
                    source_agent="dev-agent",
                    payload={
                        "wp_id": task.wp_id,
                        "error": "QA failed after retry",
                        "failed_node": "qa",
                        "runbook_url": "",
                    },
                )
            )

        return events

    def _resolve_workspace_path(self, workflow_status: dict) -> str:
        """Resolve the AgentForge workspace path from a status response."""
        sections = [
            workflow_status,
            workflow_status.get("workflow", {}),
            workflow_status.get("result", {}),
            workflow_status.get("execution", {}),
            workflow_status.get("metadata", {}),
        ]
        for section in sections:
            if not isinstance(section, dict):
                continue
            for key in _WORKSPACE_PATH_KEYS:
                value = section.get(key)
                if isinstance(value, str) and value.strip():
                    return value.strip()
        return "."

    def _infer_agent_name(self, task) -> str:
        title = (task.task_title or "").lower()
        for name in [
            "chat-agent",
            "pjm-agent",
            "sync-agent",
            "analysis-agent",
            "requirement-manager",
            "evolution-agent",
            "qa-agent",
            "dev-agent",
        ]:
            if name.replace("-", "_") in title or name.replace("-", " ") in title:
                return name
        return "dev-agent"
